
import java.util.*;

/**
 * 
 */
public class Point {

    /**
     * Default constructor
     */
    public Point() {
    }
    
    public Point(int x, int y) {
    	this._x = x;
    	this._y = y;
    }

    /**
     * 
     */
    private int _x;

    /**
     * 
     */
    private int _y;

	public int get_x() {
		return _x;
	}

	public int get_y() {
		return _y;
	}


}